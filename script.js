       function getRatioCategory(length, height) {
           const ratioLH = length / height;
           const ratioHL = height / length;
           const maxRatio = Math.max(ratioLH, ratioHL);
           if (maxRatio < 3) return "< 3:1";
           if (maxRatio <= 10) return "3:1 à 10:1";
           return "> 10:1";
       }

